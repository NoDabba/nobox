package com.wesimplify.nodabba.presentation.search;

/**
 * @author sdoddi
 *
 */
public class SearchRestaurantForm {
	
	private String cityId; 
	private String mealType;
	private String selectionKey;
	private String searchCriteria;
	private String date;
	private String people;
	private String aroundMe;
	
	/**
	 * @return the cityId
	 */
	public String getCityId() {
		return cityId;
	}
	/**
	 * @param cityId the cityId to set
	 */
	public void setCityId(String cityId) {
		this.cityId = cityId;
	}
	/**
	 * @return the selectionType
	 */
	public String getMealType() {
		return mealType;
	}
	/**
	 * @param selectionType the selectionType to set
	 */
	public void setMealType(String mealType) {
		this.mealType = mealType;
	}
	/**
	 * @return the selectionKey
	 */
	public String getSelectionKey() {
		return selectionKey;
	}
	/**
	 * @param selectionKey the selectionKey to set
	 */
	public void setSelectionKey(String selectionKey) {
		this.selectionKey = selectionKey;
	}
	/**
	 * @return the searchCriteria
	 */
	public String getSearchCriteria() {
		return searchCriteria;
	}
	/**
	 * @param searchCriteria the searchCriteria to set
	 */
	public void setSearchCriteria(String searchCriteria) {
		this.searchCriteria = searchCriteria;
	}
	/**
	 * @return the date
	 */
	public String getDate() {
		return date;
	}
	/**
	 * @param date the date to set
	 */
	public void setDate(String date) {
		this.date = date;
	}
	/**
	 * @return the people
	 */
	public String getPeople() {
		return people;
	}
	/**
	 * @param people the people to set
	 */
	public void setPeople(String people) {
		this.people = people;
	}
	/**
	 * @return the aroundMe
	 */
	public String getAroundMe() {
		return aroundMe;
	}
	/**
	 * @param aroundMe the aroundMe to set
	 */
	public void setAroundMe(String aroundMe) {
		this.aroundMe = aroundMe;
	}

	
}
